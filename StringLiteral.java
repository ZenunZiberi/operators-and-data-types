package datatypes;

public class StringLiteral {

	public static void main(String[] args) {
		System.out.println("First line\nSecond line");  // use n\ to generate new line
        System.out.println("A\tB\tC");
        System.out.println("D\tE\tF");
      
        //  Output:
        
    /*     First line
           Second line          
           A	B	C
           D	E	F      */
	}

}
