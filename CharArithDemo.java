package datatypes;

public class CharArithDemo {

	public static void main(String[] args) {
		char ch;
		
		ch = 'X';
		
		System.out.println("ch contains " + ch);
      ch++;  // a char can be incremented
      System.out.println("ch is now " + ch);
      ch = 90; // give ch the value z
      System.out.println("ch is now "+ch);
      
      
	}

}
